//Q. Find the squareroot of a number

import java.lang.Math; // or import java.lang.Math;

public class sqrt
{
	public static void main(String args[])
	{	int x,y;
		double s;
		x=20;
		y=3;
		s=Math.sqrt(x);
		System.out.println("X= "+x);
		System.out.println("Square root of x= "+s);
		System.out.println("Power of x= "+Math.pow(x,y));
	}
}